#!/bin/bash

# Проверяем, переданы ли два аргумента
if [ "$#" -ne 2 ]; then
    echo "Ошибка: требуется два аргумента."
    echo "Использование: ./sum.sh <число1> <число2>"
    exit 1
fi

# Присваиваем значения аргументов переменным 
num1=$1
num2=$2

# Вычисляем сумму
sum=$((num1 + num2))

# Выводим результат
echo "Сумма $num1 и $num2 равна: $sum"
